"""
Exercício Python 003 - Somando dois números
Crie um programa que leia dois números e mostre a soma entre eles.
"""

print ('Bem vindo ao somador de numeros inteiros!!')
number1 = int(input('Digite o primeiro numero: '))
number2 = int(input('Digite o segundo numero: '))
type1 = (type(number1))
type2 = (type(number2))
print (f'A soma do numero {number1} {type1} com o numero {number2} {type2} é:',(number1 + number2))



