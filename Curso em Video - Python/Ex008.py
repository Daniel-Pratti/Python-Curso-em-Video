"""
Exercício Python 008 - Conversor de Medidas
Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.
"""
metro = int(input('Escreva a metragem para ser convertida em centímetros e milímetros: '))
print(f'{metro} Metro(s) é equivalente a {metro*100} centímetros e {metro*1000} milímetros')